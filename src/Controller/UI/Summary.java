    public void addMessageChat(String message, int choseColor) {
        if (choseColor == 1) { // Pegunta
            this.appendToPane("PREGUNTA "+"\n"+ message + "\n", Color.DARK_GRAY);
        }else if(choseColor == 2){ // Respuesta
            this.appendToPane("RESPUESTA "+"\n"+message + "\n", Color.BLUE);
        }else if(choseColor == 3){ // Error
            this.appendToPane("ERROR "+"\n"+message + "\n", Color.red);
        }

    }

    private void appendToPane(String msg, Color c) {
        StyleContext sc = StyleContext.getDefaultStyleContext();
        AttributeSet aset = sc.addAttribute(SimpleAttributeSet.EMPTY, StyleConstants.Foreground, c);

        aset = sc.addAttribute(aset, StyleConstants.FontFamily, "Lucida Console");
        aset = sc.addAttribute(aset, StyleConstants.Alignment, StyleConstants.ALIGN_JUSTIFIED);

        int len = this.summaryClient.getDocument().getLength();
        this.summaryClient.setCaretPosition(len);
        this.summaryClient.setCharacterAttributes(aset, false);
        this.summaryClient.replaceSelection(msg);

    }
